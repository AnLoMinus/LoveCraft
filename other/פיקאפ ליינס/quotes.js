const quoteUtils = {
  getRandomQuote: (category) => {
    const quotes = additionalQuotes[category];
    return quotes[Math.floor(Math.random() * quotes.length)];
  },

  getQuotesByTopic: (searchTerm) => {
    const results = [];
    Object.values(additionalQuotes).forEach((category) => {
      category.forEach((quote) => {
        if (quote.quote.includes(searchTerm)) {
          results.push(quote);
        }
      });
    });
    return results;
  },

  getDailyQuote: () => {
    const categories = Object.keys(additionalQuotes);
    const randomCategory =
      categories[Math.floor(Math.random() * categories.length)];
    return quoteUtils.getRandomQuote(randomCategory);
  },
};
